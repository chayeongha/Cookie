package com.cookie.basic.store;

public class StoreVO {
	
	
	private int sNum; 
	private String sName; 
	private String sTel; 
	private String sSite; 
	private String Id;
	private int sOn;
	private String sNotice;
	public int getsNum() {
		return sNum;
	}
	public void setsNum(int sNum) {
		this.sNum = sNum;
	}
	public String getsName() {
		return sName;
	}
	public void setsName(String sName) {
		this.sName = sName;
	}
	public String getsTel() {
		return sTel;
	}
	public void setsTel(String sTel) {
		this.sTel = sTel;
	}
	public String getsSite() {
		return sSite;
	}
	public void setsSite(String sSite) {
		this.sSite = sSite;
	}
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}
	public int getsOn() {
		return sOn;
	}
	public void setsOn(int sOn) {
		this.sOn = sOn;
	}
	public String getsNotice() {
		return sNotice;
	}
	public void setsNotice(String sNotice) {
		this.sNotice = sNotice;
	} 
	
	
	
	

}
