package com.cookie.basic.store;

public class StoreCloseVO {

	private int ccNum;
	private int ssNum;
	private String sName;
	private String memId;
	private int cwait;

	public int getCcNum() {
		return ccNum;
	}

	public void setCcNum(int ccNum) {
		this.ccNum = ccNum;
	}

	public int getSsNum() {
		return ssNum;
	}

	public void setSsNum(int ssNum) {
		this.ssNum = ssNum;
	}

	public String getsName() {
		return sName;
	}

	public void setsName(String sName) {
		this.sName = sName;
	}

	public String getMemId() {
		return memId;
	}

	public void setMemId(String memId) {
		this.memId = memId;
	}

	public int getCwait() {
		return cwait;
	}

	public void setCwait(int cwait) {
		this.cwait = cwait;
	}

}
